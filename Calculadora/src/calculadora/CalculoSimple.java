package calculadora;

/**
 *
 * @author flocy
 */
public class CalculoSimple {

    int suma(int a, int b) {
        int sum= a + b;
        return sum;
    }

    int resta(int a, int b) {
        int res= a-b;
        return res;
    }

    float division(float a, float b) {
        float div= a/b;
        return div;
    }

    int multiplicacion(int a, int b) {
        int mul= a*b;
        return mul;
    }
    
}
